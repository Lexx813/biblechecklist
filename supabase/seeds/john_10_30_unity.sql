Pocos versículos se citan con más confianza en los debates sobre la Trinidad que Juan 10:30. Jesús dijo: "Yo y el Padre somos uno". Caso cerrado, ¿verdad? Jesús afirmó ser Dios.

Solo que eso no es lo que está ocurriendo en este pasaje. Cuando uno lee el contexto que lo rodea —y compara las palabras de Jesús con su propia oración en Juan 17— el significado se vuelve claro. Jesús no estaba afirmando ser el mismo ser que el Padre. Estaba describiendo algo mucho más significativo: unidad perfecta de propósito, voluntad y acción.

Veámoslo paso a paso.

---

## El contexto: una confrontación en el templo

El capítulo 10 de Juan relata un intercambio tenso entre Jesús y un grupo de judíos durante la Fiesta de la Dedicación en Jerusalén. Lo rodearon en el Pórtico de Salomón y le exigieron una respuesta directa:

> "¿Hasta cuándo nos vas a tener en suspenso? Si tú eres el Cristo, dínoslo francamente." — Juan 10:24

Jesús les respondió señalando sus obras —los milagros que había realizado en el nombre de su Padre— como evidencia de quién era. Luego hizo una declaración sobre sus ovejas:

> "Mis ovejas escuchan mi voz, y yo las conozco, y ellas me siguen. Yo les doy vida eterna, y de ninguna manera serán destruidas jamás, y nadie las arrebatará de mi mano. Lo que mi Padre me ha dado es algo mayor que todas las demás cosas, y nadie puede arrebatarlas de la mano del Padre." — Juan 10:27-29

Y entonces vino la declaración:

> "Yo y el Padre somos uno." — Juan 10:30

---

## ¿Qué clase de "uno"?

La palabra griega aquí es *hen* (ἕν) — género neutro, que significa "una cosa", no *heis* (εἷς) — masculino, que significaría "una persona".

Esta distinción importa. Si Jesús estuviera afirmando ser la misma persona o el mismo ser que el Padre, esperaríamos la forma masculina. En cambio, usó el neutro — señalando unidad de propósito, acción o voluntad, no identidad de persona.

Jesús acababa de decir que sus ovejas están seguras en *su* mano y en la mano *del Padre*. Dos manos. Dos personas. Pero trabajando juntas tan completamente que la protección es perfecta. Esa es la "unidad" que describe — una misión compartida, no una identidad compartida.

---

## Los judíos malentendieron — y Jesús los corrigió

Los judíos que oyeron a Jesús tomaron piedras para matarlo. Cuando él les preguntó por cuál de sus buenas obras lo apedreaban, respondieron:

> "No te apedreamos por una obra excelente, sino por blasfemia; porque tú, siendo hombre, te haces a ti mismo un dios." — Juan 10:33

Fíjese: lo acusaron de hacerse "un dios" — no de afirmar ser Jehová. Pensaban que se estaba elevando de manera indebida, no que estaba afirmando ser el Todopoderoso.

Y aquí es donde se pone interesante. Si Jesús realmente acababa de afirmar ser Dios Todopoderoso, esta era su oportunidad para confirmarlo. Pero no lo hizo. En cambio, corrigió el malentendido:

> "¿No está escrito en su Ley: 'Yo dije: "Ustedes son dioses"'? Si él llamó 'dioses' a aquellos contra quienes vino la palabra de Dios —y sin embargo la escritura no puede ser anulada—, ¿dicen ustedes a aquel a quien el Padre santificó y envió al mundo: 'Blasfemas', porque dije: 'Soy Hijo de Dios'?" — Juan 10:34-36

Jesús citó Salmo 82:6, donde Jehová llamó "dioses" a jueces humanos porque lo representaban en el juicio. Su argumento: si la Escritura usa el término "dioses" para representantes humanos imperfectos de Jehová, ¿por qué es blasfemia que aquel a quien el Padre "santificó y envió al mundo" se llame a sí mismo Hijo de Dios?

No dijo: "Sí, afirmo ser Jehová". Aclaró: "Dije que soy *Hijo* de Dios".

Eso no es una defensa trinitaria. Es una distinción.

---

## Jesús definió la "unidad" en su propia oración

Si había alguna duda sobre lo que Jesús quiso decir con "uno", la despejó en el capítulo 17 de Juan. Orando a su Padre poco antes de su arresto, Jesús dijo esto acerca de sus discípulos:

> "Hago la petición... para que todos sean uno, así como tú, Padre, estás en unión conmigo y yo estoy en unión contigo, para que ellos también estén en unión con nosotros." — Juan 17:20-21

Y de nuevo:

> "Yo les he dado la gloria que tú me has dado, para que sean uno así como nosotros somos uno. Yo en unión con ellos y tú en unión conmigo, para que sean perfeccionados en uno." — Juan 17:22-23

Léalo con atención. Jesús oró para que sus discípulos fueran "uno" de la misma manera que él y el Padre son "uno".

¿Son los discípulos un solo ser? ¿Una persona? ¿Una sustancia? Obviamente no. Son individuos separados unidos en propósito, amor y misión. Esa es la clase de unidad que Jesús comparte con su Padre — y la clase que quiere que sus seguidores compartan entre sí y con ellos.

Si "Yo y el Padre somos uno" demuestra que Jesús es Dios, entonces "para que sean uno así como nosotros somos uno" demuestra que los discípulos también son Dios. Nadie sostiene eso. Lo cual significa que la lectura trinitaria de Juan 10:30 no se sostiene.

---

## "Uno" en propósito, no en sustancia

A lo largo de los Evangelios, Jesús se describió consistentemente como subordinado al Padre, enviado por el Padre, haciendo la voluntad del Padre — no la suya propia. Este no es el lenguaje de personas coiguales de un solo ser. Es el lenguaje de un Hijo perfectamente obediente.

> "El Hijo no puede hacer ni una sola cosa por su propia iniciativa, sino solo lo que ve hacer al Padre." — Juan 5:19

> "He descendido del cielo para hacer, no mi propia voluntad, sino la voluntad del que me envió." — Juan 6:38

> "El Padre es mayor que yo." — Juan 14:28

> "¿Por qué me llamas bueno? Nadie es bueno sino uno, Dios." — Marcos 10:18

Estas declaraciones son difíciles de reconciliar con la idea de que Jesús es coigual al Padre. Pero tienen perfecto sentido si Jesús es el Hijo de Dios — distinto del Padre, subordinado a él, pero unido con él en propósito y acción tan completamente que ver trabajar a Jesús es ver la voluntad del Padre hecha realidad.

Eso es lo que Jesús quiso decir en Juan 10:30. Esa es la "unidad" que describió.

---

## ¿Y lo del apedreamiento?

Los trinitarios a veces argumentan que la reacción violenta de los judíos demuestra que Jesús *estaba* afirmando ser Dios. ¿Por qué si no intentarían apedrearlo por blasfemia?

Pero fíjese en lo que Jesús hizo a continuación. No confirmó su conclusión — la corrigió. Señaló la Escritura, mostró que el término "dioses" se ha aplicado a humanos en ciertos contextos, y aclaró que se estaba llamando a sí mismo Hijo de Dios.

Los judíos estaban equivocados acerca de lo que él afirmaba. Su enojo no valida su interpretación.

La gente malinterpreta las cosas todo el tiempo. A lo largo de los Evangelios, los líderes religiosos repetidamente malinterpretaron a Jesús — acusándolo de estar poseído por demonios, de quebrantar el sábado ilegalmente, de ser un blasfemo. Su malentendido no establece la verdad; revela su incapacidad para comprender quién era Jesús realmente.

---

## Padre e Hijo: unidos, no idénticos

Cuando Jesús dijo "Yo y el Padre somos uno", nos dio una visión profunda de su relación con Jehová. El Hijo y el Padre trabajan en completa armonía. Sus propósitos se alinean perfectamente. Lo que Jesús hace refleja exactamente lo que el Padre quiere. La protección que Jesús da a sus ovejas está respaldada por todo el poder del Padre.

Pero nada de eso requiere que sean el mismo ser.

Un esposo y una esposa pueden ser "una sola carne" (Génesis 2:24) sin ser una sola persona. Los primeros cristianos eran "uno de corazón y alma" (Hechos 4:32) sin fusionarse en un solo individuo. Jesús y el Padre son "uno" en la unidad más profunda posible de propósito, amor y misión — mientras permanecen como dos personas distintas.

Esa no es una clase menor de unidad. De hecho, es más impresionante. Dos voluntades, perfectamente alineadas. Dos personas, trabajando como una.

