---

## Lo que esto significa

La unidad de Jesús con el Padre es algo para maravillarse, no algo para reducir a una fórmula filosófica. Él es el Hijo — no el Padre usando una máscara diferente, no un segundo modo del mismo ser, sino una persona distinta tan perfectamente alineada con su Padre que pudo decir: "El que me ha visto a mí, ha visto también al Padre" (Juan 14:9).

No porque sean idénticos. Porque el Hijo refleja al Padre tan impecablemente.

Esa es la relación que presenta la Escritura. Y es mucho más convincente que la fórmula abstracta de "una sustancia, tres personas" forjada en concilios del siglo IV. Jesús no estaba haciendo filosofía. Estaba revelando quién es — el Hijo amado, enviado por su Padre, unido con él en propósito, amor y misión.

